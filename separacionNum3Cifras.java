// Enunciado: Programa que ingrese un numero de 3 cifras y devuelva las 3 cifras por separado

package ejercicios;

import java.util.Scanner;

public class Ejercicios {
    
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Ingrese un numero de 3 cifras: ");
        var numero = Integer.parseInt(sc.nextLine());
        System.out.println("La primer cifra de " + numero + " es " + (numero/100));
        System.out.println("La segunda cifra de " + numero + " es " + (numero/10)%10);
        System.out.println("La tercera cifra de " + numero + " es " + (numero%10));
    }
}
